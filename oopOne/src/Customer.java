public class Customer {
    private int id;
    private String costomerNumber;
    private String phone;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCostomerNumber() {
        return costomerNumber;
    }

    public void setCostomerNumber(String costomerNumber) {
        this.costomerNumber = costomerNumber;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }
}
